    static GPSPoint mGPSPoint ;
    public static void startNavi(GPSPoint point){
        //TODO 这版本暂时不要校准
        if (!AppUtils.isForeground(KApplication.sContext,WalkRouteNaviActivity.class.getName())) {
            mGPSPoint = point;
            Intent intent = new Intent(KApplication.sContext, WalkRouteNaviActivity.class);
            KApplication.sContext.startActivity(intent);
        }
    }
